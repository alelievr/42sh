#include "libft.h"

inline void				ft_dbg(char *file, int line)
{
	char				*f;

	f = strrchr(file, '/');
	f = (f == NULL ? file : f + 1);
	ft_printf("%{BF}[ %{bF}Debug%{!bF}: %{F}%s%{F}: %{F}%d%{F} ]%{0}\n",
			154, 0, 9, 0, 125, f, 0, 125, line, 0);
}
