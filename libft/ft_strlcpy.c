#include "libft.h"

size_t		ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	i;

	i = 0;
	while (i + 1 < size && *src)
		dst[i++] = *src++;
	dst[i] = 0;
	return (strlen(src) + i);
}
